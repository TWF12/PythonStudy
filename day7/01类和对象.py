# 定义一个类
class Student:
    name = None
    gender = None
    nationality = None
    native_place = None
    age = None

# 创建对象
stu_1 = Student()

# 属性赋值
stu_1.name = "林俊杰"
stu_1.gender = "男"
stu_1.nationality = "中国"
stu_1.native_place = "山东省"
stu_1.age = 31

# 输出
print(stu_1.name)
print(stu_1.gender)
print(stu_1.nationality)
print(stu_1.native_place)
print(stu_1.age)
