# 标识符命名规则
# 1. 标识符只能包含字母、数字、下划线，且不能以数字开头
# 2. 标识符不能使用Python的关键字和内置函数名
# 3. 标识符不能包含空格
# 4. 标识符区分大小写
# 5. 标识符应具有描述性，见名知意
# 6. 标识符一般使用小写字母，多个单词使用下划线连接
# 7. 标识符一般使用英文，避免使用拼音和中文
# 8. 标识符长度适中，一般不超过20个字符
# 9. 避免使用单字符标识符，除非在特定情况下（如循环变量）
# 10. 避免使用下划线开头的标识符，除非有特殊含义（如私有变量）
# 11. 避免使用双下划线开头和结尾的标识符（如魔术方法）
# 12. 避免使用与常见库冲突的标识符
# 13. 使用驼峰命名法（CamelCase）来命名类
# 14. 使用全大写字母和下划线来命名常量
# 15. 保持一致的命名风格，遵循团队或项目的命名规范
# 16. 使用有意义的缩写，避免过度缩写
# 17. 避免使用容易混淆的字符（如数字0和字母O）


